//Asignando valores a variables con operador de incremento
var numUno = 10;
++numUno; //Es lo mismo que numUno = numUno + 1
console.log(numUno);

//Asignando valores a variables con operador de decremento
var numDos = 20;
--numDos; //Es lo mismo que numDos = numDos - 1
console.log(numDos);

//Asignando valores booleanos o lógicos
var estado = true;
console.log(estado);

var tipo = false;
console.log(tipo);

//Operador de negación
console.log(!estado);
console.log(!tipo);

//Operador de residuo
var primNum = 100;
var segNum = 55;
resultado =  primNum % segNum;
console.log(resultado);

//Operadores de comparación
var numero1 = 15;
var numero2 = 18;
respuesta01 = numero1 < numero2;
respuesta02 = numero1 > numero2;
respuesta03 = numero1 == numero2;
respuesta04 = numero1 != numero2;
console.log(respuesta01);
console.log(respuesta02);
console.log(respuesta03);
console.log(respuesta04);
